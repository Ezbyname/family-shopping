export function lookupDictionary(hebrewTerm, userDict = {}) {
  const term = hebrewTerm.trim();
  // User dict has highest priority
  if (userDict[term]) return { found: true, result: userDict[term], source: 'user' };
  if (GLOBAL_DICTIONARY[term]) return { found: true, result: GLOBAL_DICTIONARY[term], source: 'global' };
  return { found: false, result: null, source: null };
}