export function resolveCity(rawCode) {
  const code = String(rawCode ?? '').trim();
  if (!code || code === '0' || code === '0000' || code === '00000')
    return { cityId: code || '0', cityName: null, cityResolutionSource: 'unknown' };
  const cityName = CBS_TABLE.get(code) ?? null;
  return { cityId: code, cityName, cityResolutionSource: cityName !== null ? 'cbs_mapping' : 'unknown' };
}
export function knownCodeCount() { return CBS_TABLE.size; }
